var color = ["#ff0000","#ff0000"];
var qFix = ["#000000","#000000"];
var i = 2;

document.querySelector(".button1").addEventListener("click",
function(){ 
i = i < color.length ? ++i : i - 2;
i = i < qFix.length ? +i : i - 2;

document.querySelector("body").style.background = color[i]
document.querySelector(".p3").style.background = qFix[i]
})

